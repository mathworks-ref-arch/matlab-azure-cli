    fprintf('%s\n', 'Group')
    fprintf('%s\n', '    az backup protection : Manage protection of your items, enable protection or disable it, or take')
    fprintf('%s\n', '    on-demand backups.')
    fprintf('%s\n', '        Command group ''backup'' is in preview. It may be changed/removed in a future')
    fprintf('%s\n', '        release.')
    fprintf('%s\n', 'Commands:')
    fprintf('%s\n', '    auto-disable-for-azurewl  : Disable auto-protection for the specified item.')
    fprintf('%s\n', '    auto-enable-for-azurewl   : Automatically protect all existing unprotected DBs and any DB which')
    fprintf('%s\n', '                                will be added later with the given policy.')
    fprintf('%s\n', '    backup-now                : Perform an on-demand backup of a backed up item.')
    fprintf('%s\n', '    check-vm                  : Find out whether the virtual machine is protected or not. If')
    fprintf('%s\n', '                                protected, it returns the recovery services vault ID, otherwise it')
    fprintf('%s\n', '                                returns empty.')
    fprintf('%s\n', '    disable                   : Stop protecting a backed up item. Can retain the backed up data')
    fprintf('%s\n', '                                forever or choose to delete it.')
    fprintf('%s\n', '    enable-for-azurefileshare : Start protecting a previously unprotected Azure File share within an')
    fprintf('%s\n', '                                Azure Storage account as per the specified policy to a Recovery')
    fprintf('%s\n', '                                services vault.')
    fprintf('%s\n', '    enable-for-azurewl        : Start protecting a previously unprotected workload within an Azure')
    fprintf('%s\n', '                                VM as per the specified policy to a Recovery services vault. Provide')
    fprintf('%s\n', '                                the workload details as a protectable item.')
    fprintf('%s\n', '    enable-for-vm             : Start protecting a previously unprotected Azure VM as per the')
    fprintf('%s\n', '                                specified policy to a Recovery services vault.')
    fprintf('%s\n', '    resume                    : Resume backup for the associated backup item. Use this to change the')
    fprintf('%s\n', '                                policy associated with the backup item.')
    fprintf('%s\n', '    undelete                  : Rehydrate an item from softdeleted state to stop protection with')
    fprintf('%s\n', '                                retained data state.')
    fprintf('%s\n', '    update-for-vm             : Update disk exclusion settings associated with a backed up VM item.')
    fprintf('%s\n', 'For more specific examples, use: az find "az backup protection"')
    fprintf('%s\n', 'Please let us know how we are doing: https://aka.ms/azureclihats')
end
